<p><span style="font-weight: 400;">Рабочий день в компании является нормированным. При этом руководство спрашивает с сотрудников не только за присутствие на рабочем месте, а и за объем выполненной, согласно плану, работы. Базовый оклад, который выплачивается сотруднику, выплачивается за использованные в дело 480 минут в день, пять дней в неделю. При необходимости руководитель может потребовать от сотрудника вести планирование рабочего дня (методом &laquo;Альпы&raquo;), хронометраж рабочего дня (фиксация каждой смены деятельности и времени перехода) и ежедневные отчеты о результатах.</span></p>
