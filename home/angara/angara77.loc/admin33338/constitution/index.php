<p><span style="font-weight: 400;">Если сотрудник подтвердил, устно или письменно, что распоряжение он понял и выполнит, и никаких возражений, вопросов, комментариев не поступило (или они были разобраны, и сотрудник подтвердил, что с ними разобрались), любые претензии, предъявленные после согласия, связанные с невыполнением или неверным выполнением, не принимаются. В том числе &ndash; если сотрудник не понял, но не решился про это сказать. Ответственность за это лежит на сотруднике. Сказав &laquo;понял&raquo; или &laquo;сделаю&raquo; - ты отвечаешь за свои слова.</span></p>
