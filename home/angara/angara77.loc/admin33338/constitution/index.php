<h3><span style="font-weight: 400;">Решение поднятых вопросов</span></h3>
<p><span style="font-weight: 400;">Все поднятые вопросы &ndash; решаются, а не остаются в подвешенном состоянии. По каждому поднятому вопросу руководителем назначается ответственный и контрольный срок решения. Если к сроку вопрос не решен &ndash; решение принимается либо немедленно руководителем, либо вопрос откладывается на продолжительное фиксированное время, и до истечения этого срока &ndash; не поднимаются вновь.</span></p>
